                    <div class="mbr-text col-12 col-md-8 mbr-fonts-style display-7">
                        <div>
                            <strong>· 1913 - </strong>£250.00 was paid to lay the first bowling green in Krugersdorp.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1914 – </strong>The town Engineer was instructed to start levelling the ground.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1915 – </strong>Mr. Sharp (curator of parks board) reported that the green was
                            ready to be handed over to the club, a wood shelter and an iron hut was also erected at a
                            cost of £50.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1916 – </strong>A draft constitution was submitted and adopted with annual fees of
                            £2.20, per year. Monthly players and visitors would be able to use the greens for £0.76 per
                            month. Ladies would also be able to play for a fee of £0.16, but not on Wednesdays; Saturday
                            afternoons and Sundays.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1916 – </strong>The Honorary presidents were Sir Abe Bailey & Mr. J.B. Robinson
                            and the first elected President was Mr. J. Richardson.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1916 – </strong>The first league game was played in Pretoria against a team from
                            Brakpan.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1917 – </strong>The club was officially opened by Sir Abe Bailey, and so
                            Krugersdorp Bowling Club was BORN.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1917 – </strong>G.W. Dixon and T. Lightbody won the Transvaal District Pairs.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1922 – </strong>A. Know; J. Clark; D. McGregor and D. Cameron won the Transvaal
                            District fours.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1926 - </strong>Mr. “Pinky” Danilowitz became the South African as well as the
                            Commonwealth Games Singles Champion. He played 82 consecutive games without losing.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1945 – </strong>The 2nd green was opened by the then Mayor, Mrs. A. Edwards, and
                            was duly named the Harry Sharp Green.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1951 – </strong>Mr Sharp was duly elected a Honorary life member, he had been a
                            member since 1915.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1953 – </strong>Krugersdorp hosted the “Australian bowlers”.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1969 – </strong>The local press reported that a “freak” wind lifted 2 Krugersdorp
                            bowlers off their feet and dumped them 10 feet away.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1970 – </strong>Bar prices increase to 20c; beer shandy to 26c and Brandy; Gin &
                            Cane to 16c.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1975 – </strong>We celebrated our 60th anniversary with a cocktail party at the
                            clubhouse.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1977 – </strong>The birth of the “gold rush” day was founded, with 2 other fund
                            raisers, being the “Ladies Day” & the “Nedbank Day” also done for the first time that year.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1980 – </strong>The year a decision was made by the council to move the club to
                            its present location.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 1984 – </strong>On 4th August the official closing of the “old” club in
                            Krugersdorp Town was done and on the 20th October the “new” clubhouse; ground and greens
                            were officially opened by the Mayor of Krugersdorp.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 2001 – </strong>This was the year that the club made a decision to unify the men’s
                            and ladies sections, and the first President under the new format was Mr. Bill Shead.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 2005 – </strong>Mr. Rusty Whittaker, after writing the clubs newsletter for 13
                            years, decided enough was enough, and stopped the newsletter early in the next year.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 2005 – </strong>Our clubs museum was completed by Jan Hoenselaar.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 2006 – </strong>23 liters of “old brown” sherry was consumed when the LADIES held
                            the District bowls between the 19th & 23rd May.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 2007 – </strong>Krugersdorp had 159 members of which 92% were over the age of 60.
                            Of that 92%, 12% were even over the age of 80.
                        </div>
                        <div><br></div>
                        <div>
                            <strong>· 2016 – </strong>We, the “new” members of Krugersdorp Bowling Club hope that we can
                            uphold the enormous tradition that our club has seen, in the next 100 years.
                        </div>
                    </div>
                </div>
