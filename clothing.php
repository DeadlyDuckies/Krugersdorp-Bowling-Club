                                    <br>
                                    <br>
                                    <strong>2. Blouse or Shirts</strong>
                                    <br>
                                    A club shirt, white blouse or white shirt of the golf type with conventional
                                    collar and sleeves may be worn. No blouse or shirt may be worn without a collar.
