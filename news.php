                                    <p class="mbr-fonts-style panel-text display-7"><strong>Fellow Members</strong><br>From
                                        a personal point of view part of May, June and Part of July is best forgotten about,
                                        but here we are into August already. The winter months are almost behind us and
                                        spring is around the corner.
                                        <br>There are a few items I have to bring to your attention.
                                        <br><br><strong>Club News
                                        </strong><br>Our AGM is taking place at the club on the Saturday 30th September 2017
                                        at 14h00. Closing date for nominations is the 1st September. After taking a look at
                                        the notice board I see that only the Executive Members e.g. President, Vice
                                        President, Treasurer and Secretary and four committee members are up for election at
                                        this time. It is the time of the year when all of us should be thinking about our
                                        club representatives for the coming season. For our new members, remember that
                                        although someone has been nominated for a particular position, should there be more
                                        than the required number of nominees for that position on the day of the AGM; it
                                        will go to the vote. (E.g. if we require six nominations for the main committee and
                                        there are seven nominees the members present will be asked to vote for the six they
                                        think will be the most suitable)
                                        <br>It is important to remember that should you accept your nomination, your full
                                        commitment for the whole year would be appreciated.
                                        <br>I was asked by a player whether or not they would be eligible to be a Club
                                        Selector. For the position of Club Selector, both Ladies and Men, you will be
                                        required to meet certain requirements. These are printed on the nomination form for
                                        Club Selector.
                                        <br>Do not forget to pay your subs or part thereof prior to the date of the AGM.
                                        This will allow you to take part in any of the discussions and any voting that may
                                        be required. Remember that you may pay your subs over a period of time so please
                                        discuss and make the necessary arrangements with the President, Hon. Treasurer or
                                        Hon. Secretary<br><br><strong>Club Competitions
                                        </strong><br>Club competitions have been completed and our Accolades evening will
                                        take place on Friday 18th August 2017 from 17h30. <br><br><strong>League and
                                            Selection
                                        </strong><br>The league lists for both the Ladies and Men are on the notice board.
                                        We have had two league practice sessions, one I thought was quite successful the
                                        second not so good, mainly due to lack of attendance.
                                        <br>Your current selectors have a meeting on the 3rd August to discuss the way
                                        forward, with regard to practice sessions. Sides will probably be selected prior to
                                        the day to ensure a good attendance. However I will keep you informed as the
                                        decision of the meeting.<br><br><strong>Talking Point – Observations
                                        </strong><br>I have not been around much during the past few weeks to play bowls but
                                        on occasions I have watched many of you play. I have been impressed on the steady
                                        improvement in the bowls, both in consistency and accuracy. Well done, keep it up.
                                        Since I’ve been back, playing I also took particular notice on the way players were
                                        reading the head and calling the shots, and again, well done
                                        <br>During the Drawn Pairs last weekend, “slow play” was brought up. If we deem this
                                        to be necessary it should be included in our Conditions of Play. I personally have
                                        never had to refer to the “slow play “clause. From what I know and read in the Laws
                                        of The Game, this features mainly in games that are being played to a time limit
                                        e.g. a fifteen end game must be completed in two and a quarter hours, or by a
                                        certain time, where the side winning the game could play for time. I am not going
                                        into the rules here it would take up too much space, however should anyone wish to
                                        talk about it, please speak to me or one of the our other umpires. (In the new Laws
                                        of the Game, Third Edition (yellow) it is clause A5 – Delaying (slow) Play)
                                        <br>
                                        <br>One of the problems with our coaching is that we tell the new bowler to take
                                        their time on the mat ensuring that your feet, stance, bowl, delivery and line are
                                        all correct. Maybe we should also tell them that they must bowl their bowl without
                                        any undue delay, thirty-forty seconds should be plenty of time
                                        <br><br><strong>Sables</strong><br>Many of you will have heard a rumour going around
                                        regarding the future of Sables District. Our present District Executive will know
                                        more about what is likely to happen after their next two meetings, one with the
                                        other districts concerned and Gauteng Bowls and the other with Government
                                        representatives. By mid-August we should have some idea of what is going to
                                        transpire.
                                        <br>At this moment in time, let me say what I understand may happen in the near
                                        future.
                                        <br>At present Bowls SA have nineteen or twenty Districts e.g. Sables, Ekurhuleni,
                                        Port Natal, Limpopo, North West etc. In order for the government to allocate funds
                                        towards sport in the current nine Provinces, all sporting / bowling clubs will have
                                        to fall within one or other of the nine provinces.
                                        <br>It just so happens that at this time, in Sables, Maraisburg, Florida North,
                                        Leases, Discovery, RCC, Witpoortjie, and Honeydew Bowling Clubs fall within
                                        Johannesburg Municipal Boundary of Gauteng. Logically this may mean that they will
                                        be no longer part of Sables and will play their League, District Competitions and
                                        Provincials as part of, and be subject to, the conditions applicable to JBA (Bowls
                                        Gauteng).
                                        <br>Rustenburg will, I assume now fall under North West.
                                        <br>The remaining Sables Bowling Clubs, Krugersdorp, Noordheuwel and Randfontein,
                                        are located in Mogale City and therefore not eligible, at this time to play in the
                                        Province of Gauteng. These three clubs will be all that is left of the Sables
                                        Bowling Association.
                                        <br>Needless to say, there will be many discussions coming up and decisions will
                                        have to be made and negotiated over the next few months, with regard to the future
                                        of bowls in and around the areas of Roodepoort, Johannesburg and Krugersdorp.
                                        <br>The outcome of these meetings and the decisions made will be of vital importance
                                        to our club. Members will be kept informed of the progress on a regular basis. I can
                                        see many special meetings taking place to ensure that the bowls at Krugersdorp is
                                        not compromised in any way.<br><br>Since I started this Newsletter, our President,
                                        Cobus, has spoken to the Sables District President, Mark Petzer and has been
                                        informed that this transformation may take up to five years to materialise. As I
                                        have said we will know more after the meeting on the 14th August.
                                        <br>There is some inevitability about the situation and we must prepare ourselves
                                        for some changes to our Sables District and the future of bowls.
                                        <br><br><strong>Greens</strong><br>“B” will remain open for social bowls whilst the
                                        other two receive their spring treatment
                                        <br>That’s all for now folks,
                                        <br>
                                        <br>Alan Lyall<br></p>
