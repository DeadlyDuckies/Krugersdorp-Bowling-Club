                        <div>
                            <strong>1. PREAMBLE</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            1.1 The name of the club shall be “THE KRUGERSDORP BOWLING CLUB”. The use hereinafter of the word
                            “CLUB” shall mean the Krugersdorp Bowling Club and the use of the word “CONSTITUTION” shall mean the
                            Constitution of the Krugersdorp Bowling Club.
                        </div>
                        <div>
                            1.2 The provisions of the Constitution and the by-laws of the Club shall bind all members of the
                            club.
                        </div>
                        <div>
                            1.3 The constitution and the by-laws are not gender specific.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>2. OBJECTIVES</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            The Club is promoted for the purpose of:
                        </div>
                        <div>
                            2.1 Playing the game of Lawn Bowls and to promote recreation and social enjoyment in connection
                            therewith as also the development of the sport of Lawn Bowls;
                        </div>
                        <div>
                            2.2 The Club will as far as possible become and remain active in the community of Krugersdorp;
                        </div>
                        <div>
                            2.3 The Club shall be affiliated to Bowls South Africa through the Sables Bowling Association;
                        </div>
                        <div>
                            2.4 The Club shall abide by the laws of the game as laid down by Bowls South Africa;
                        </div>
                        <div>
                            2.5 The club shall have a separate legal personality and be capable of perpetual succession, i.e.
                            not be dependent upon the identity or number of its members;
                        </div>
                        <div>
                            2.6 The club shall be capable of holding property and assets, i.e. apart from the property and
                            assets of its individual members;
                        </div>
                        <div>
                            2.7 The club shall not have as its object the acquisition of gain for itself or its individual
                            members;
                        </div>
                        <div>
                            2.8 The club shall be entitled to sue or be sued in it’s own name;
                        </div>
                        <div>
                            2.9 The club’s income and property shall not be distributable to its members or office bearers
                            except as reasonable compensation for services rendered;
                        </div>
                        <div>
                            2.10 The club shall accept and abide by the spirit and terms of the South African Institute
                            for Drug Free Sport (SAIDFS) Anti-Doping Programme and the Anti-Doping Rules, including the
                            application of its sanctions to individuals and shall respect the authority of and co-operate
                            with SAIDFS and the hearing bodies in all anti-doping matters which are not governed by the
                            rules of the relevant International Federation;
                        </div>
                        <div>
                            2.11 The club and its members shall abide by all agreements entered into between Bowls South
                            Africa and any authority relating to the promotion, control or testing of prohibited
                            substances and drug use by any member of any club in the republic of South Africa;
                        </div>
                        <div>
                            2.12 The club through its committee shall be obliged to take appropriate disciplinary action
                            against any of its individual members who contravene any applicable code of conduct, rule,
                            bylaw or resolution by the club committee, Sables District or Bowls SA or who commits any act
                            deemed to be harmful to the sport of bowls or any other member or person at any club, bowls
                            facility or bowls event or any other act which brings the sport into disrepute.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>3. MANAGEMENT COMMITTEE</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            3.1 The affairs of the Club shall be administered by a Committee, which shall hereinafter be
                            referred to as THE MANAGEMENT COMMITTEE”.
                        </div>
                        <div>
                            3.2 The Management Committee shall consist of an Executive Committee comprising of:
                        </div>
                        <div>
                            3.2.1 A President, who must have served on the Management Committee for the year immediately prior
                            to election;
                        </div>
                        <div>
                            3.2.2 A Vice President;
                        </div>
                        <div>
                            3.2.3 An Honorary Secretary;
                        </div>
                        <div>
                            3.2.4 An Honorary Treasurer;
                        </div>
                        <div>
                            and at least five (5) and a maximum of seven (7) ordinary members.
                        </div>
                        <div>
                            3.3 All members of the Management Committee shall retire annually but are eligible for re-election.
                        </div>
                        <div>
                            3.4 No Club member of less than one (1) years standing shall be eligible for election to the
                            Management Committee.
                        </div>
                        <div>
                            3.5 A quorum for meetings of the Management Committee shall be six (6).
                        </div>
                        <div>
                            3.6 The Management Committee shall be empowered to co-opt additional members where it is deemed
                            necessary and in the interest of the Club.
                        </div>
                        <div>
                            3.7 The Management Committee shall meet as often as is necessary to discharge its duties.
                        </div>
                        <div>
                            3.8 At least seven (7) days notice of meetings shall be given to all Committee members stating the
                            date, time and place of all such meetings.
                        </div>
                        <div>
                            3.9 On the written request of four (4) members of the Management Committee, stating the reason, a
                            special meeting of the Management Committee shall be called subject to the provisions of sub clause
                            3.8 hereof.
                        </div>
                        <div>
                            3.10 Notwithstanding the foregoing, the President shall have the right, in a matter of urgency, to
                            cause a meeting of the Management Committee to be called with less than seven days notice subject,
                            however, at all time to the provision of sub clause 4.2.11
                        </div>
                        <div>
                            3.11 At all meetings of the Management Committee, the President, or in his/her absence, the
                            Vice-President, shall be Chairperson. Should both be absent the Management Committee shall elect a
                            Chairperson from its members who are present for such meeting.
                        </div>
                        <div>
                            3.12 All decisions made at any Management Committee meeting shall be decided by a majority vote.
                        </div>
                        <div>
                            3.13 In the case of an equality of votes the Chairperson shall have a casting vote in addition to
                            his/her deliberative vote.
                        </div>
                        <div>
                            3.14 Any member of the Management Committee, who is absent from three (3) consecutive meetings
                            without having received leave of absence or without reasons deemed satisfactory to the Management
                            Committee, shall cease to be a member thereof.
                        </div>
                        <div>
                            3.15 The remaining members of the committee may fill any casual vacancy occurring in the Management
                            Committee.
                        </div>
                        <div>
                            3.16 Should the members of the Management Committee for any reason whatever resign en bloc or be
                            reduced to numbers so as not to leave a quorum, twelve (12) full members of the Club in good
                            standing shall be empowered to convene a Special General Meeting for the election of a caretaker
                            committee to serve out the unexpired period of the period of office caused by either of the
                            situations set out above.
                        </div>
                        <div>
                            3.17 All members of the Management Committee shall be indemnified out of the club’s funds against
                            all costs, charges, expenses, losses and liabilities incurred by him in the conduct of the club’s
                            business or the discharge of his duties or from any loss on any grounds other than his own willful
                            acts or defaults.
                        </div>
                        <div>
                            <strong>
                                <br>
                            </strong>
                        </div>
                        <div>
                            <strong>4. POWERS OF THE MANAGEMENT COMMITTEE</strong>
                        </div>
                        <div>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            4.1 The management and control of the club and the carrying out of its objects shall be vested in
                            the Management Committee.
                        </div>
                        <div>
                            4.2 The Management Committee shall have the following powers:
                        </div>
                        <div>
                            4.2.1 To make amendments to or rescind By-Laws not inconsistent with this Constitution. All By-Laws
                            so made, amended or rescinded shall be posted on the notice board of the club for a period of
                            fourteen (14) days before becoming effective;
                        </div>
                        <div>
                            4.2.2 To constitute Sub Committees for specific purposed and delegate to such Sub Committees the
                            appropriate powers and authority to carry out their terms of reference. At least one (1) member of
                            the Management Committee shall serve on each Subcommittee;
                        </div>
                        <div>
                            4.2.3 To institute or defend legal proceedings in the name of the Club. To appoint the President or
                            some other member or members of the Management Committee to act on behalf of the Club in instituting
                            or defending proceedings in a Court of Law or other forum;
                        </div>
                        <div>
                            4.2.4 To engage attorneys, accountants or agents to act on behalf of the Club;
                        </div>
                        <div>
                            4.2.5 To engage employees either on a permanent or temporary basis and to determine the terms and
                            conditions of employment applicable to such employees consistent with current legislation, and shall
                            further have the right and authority to suspend or terminate such employment;
                        </div>
                        <div>
                            4.2.6 To apply for or renew any license or permits which are necessary for the sale of liquor or
                            any commodities on the Club premises, or for the carrying out of any other activity on the Club
                            premises;
                        </div>
                        <div>
                            4.2.7 To invest or deposit funds of the Club in Banks, Building Societies or other approved
                            financial institutions;
                        </div>
                        <div>
                            4.2.8 To borrow or raise money from any person or body of persons whether corporate or incorporate
                            for the purpose of managing the affairs of the Club. The President and the Honorary Treasurer for
                            the time being shall be authorized to sign any promissory note, Bill of Exchange or other document
                            on behalf of and binding the Club to repay the amount so borrowed or raised provided such loan has
                            been authorized by the Management Committee;
                        </div>
                        <div>
                            4.2.9 To reciprocate any privileges offered by any other Club;
                        </div>
                        <div>
                            4.2.10 To cause minutes to be kept of all its meetings. All such minutes shall be considered at the
                            next succeeding meeting and, if adopted, signed as a true record by the Chairperson and Secretary of
                            the meeting. A copy of such minutes shall be sent to each Committee member within twenty-one (21)
                            days after the date of the meeting to which the minutes refer;
                        </div>
                        <div>
                            4.2.11 To deal with any emergency which requires immediate action the Executive shall deal
                            with the matter in such manner, as it thinks fit and shall report its actions to the next meeting of
                            the Management Committee for ratification;
                        </div>
                        <div>
                            4.2.12 To be responsible for the safeguarding and maintenance of all assets of the Club. No
                            property shall be removed from the Club premises without permission having first been obtained from
                            the Management Committee, and the entry recorded in the Asset Register.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>5. DUTIES OF OFFICE BEARERS</strong>
                        </div>
                        <div>
                            5.1 The Honorary Secretary shall convene all meetings of the Management Committee and shall keep
                            minutes recording the business transacted at these meetings. He/she shall also maintain and keep up
                            to date a list of names and addresses of members. The Honorary Secretary shall, in addition, perform
                            those duties allocated to him/her by the Management Committee, and not inconsistent with the
                            Constitution.
                        </div>
                        <div>
                            5.2 The Honorary Treasurer shall be responsible for the collection of monies due to the Club.
                            He/she shall also pay all normal day to day accounts due, or extraordinary accounts on instructions
                            of the Management Committee. He/she shall keep proper books of accounts at all times. He/he shall
                            promptly deposit all money accruing to the Club and prepare and submit a statement of Income and
                            Expenditure together with a Bar Trading Account to every monthly meeting of the Management
                            Committee. He/she shall, at the end of the financial year, prepare an Income and Expenditure Account
                            and Balance Sheet and have these duly certified by the Club’s Honorary Examiner for submission to
                            the Annual General Meeting.
                        </div>
                        <div>
                            5.3 The Honorary Examiner/s shall with due diligence examine all books and vouchers pertaining to the financial
                            affairs of the Club and shall issue a certificate as to their correctness or otherwise. No Honorary
                            Examiner/s shall be a member of the Management Committee.
                        </div>
                        <div>
                            5.4 All records, correspondence, Books of Account, documents etc., shall remain the property of the
                            Club, and the Office Bearer concerned shall ensure their safe keeping; and shall be responsible for
                            handing over same at the end of his term of office.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>6. SELECTION COMMITTEE</strong>
                        </div>
                        <div>
                            6.1 The Selection Committee shall comprise of seven (7) ordinary members, at least two (2)
                            of whom shall be of each gender for the selection of ladies’ and men’s teams which members
                            shall each comply with the conditions that they shall have played bowls for at least three
                            (3) years and in addition have been a member of the club for at least two (2) years.
                        </div>
                        <div>
                            6.2 The Convener for such committee shall be appointed from the selectors by the Management
                            Committee elected by the members of such Committee and shall report to the Management
                            Committee when so arranged or requested to do so by the Hon. Secretary or President.
                        </div>
                        <div>
                            6.3 A quorum for meetings of the Selection Committee shall be five (5).
                        </div>
                        <div>
                            6.4 A function of the Selection Committees shall be to determine the gradings and handicaps of
                            members, and to select all teams to represent the Club, other than in open competitions.
                        </div>
                        <div>
                            6.5 The Selection Committees shall meet as and when required and the Convener shall convene such
                            meetings by giving notice thereof to each member in person by whatever means he/she deems expedient.
                        </div>
                        <div>
                            6.6 It is incumbent upon the Selection Committees to recommend to the Management Committee, whether
                            or not a team or teams who wish to enter as representatives of the Club at the Bowls South Africa
                            National Bowling Tournament are suitable. The decision of the Management Committee shall be final.
                        </div>
                        <div>
                            6.7 Only members of at least six (6) months standing shall be eligible for selection to the team/s
                            to represent the Club at the Bowls South Africa National Tournament.
                        </div>
                        <div>
                            6.7 The provisions of clause 3.14 shall be mutatis mutandis applicable to members of the
                            Selection Committee with regard to attendance at meetings.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>7. CLASS OF MEMBERS</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            7.1 Membership of the Club shall be confined to the following:
                        </div>
                        <div>
                            7.1.1 ORDINARY MEMBERS, who have been accepted by the Management Committee after the provisions of
                            sections 8.1 to 8.5 have been complied with, shall be classified as Ordinary Members. Ordinary
                            Members shall have full privileges of the Club subject to the Constitution and By-Laws.
                        </div>
                        <div>
                            7.1.2 HONORARY LIFE MEMBERS. The Management Committee shall be entitled to recommend that Honorary
                            Life Membership of the Club be bestowed upon any person whom it deems to be worthy of the honour for
                            having conferred some special benefit upon the Club or for service to the Club or for any other
                            reason the Management Committee thinks fit. Such recommendation shall be dealt with at the Annual
                            General Meeting.
                        </div>
                        <div>
                            7.1.3 PROSPECTIVE MEMBERS shall be all persons who are eligible as Ordinary Members and who have
                            made a bona fide application for membership. Such members shall cease to be Prospective Members
                            immediately the Management Committee has decided on their application for Ordinary Membership. Until
                            the membership status has been decided, the applicant shall be accorded the full privileges of an
                            Ordinary Member, subject to the rules and regulations of the Club.
                        </div>
                        <div>
                            7.1.4 SOCIAL MEMBERS. The Management Committee shall have the right to grant Social Membership to:
                            a) Ordinary Members of the Club who are no longer active but wish to continue to enjoy the social
                            amenities of the Club and
                        </div>
                        <div>
                            b) Any other person who, through association or for any acceptable reason, wishes to enjoy the
                            amenities of the Club. Social Members shall neither hold office, vote, nor have any voice in the
                            Management of the Club.
                        </div>
                        <div>
                            7.1.5 TEMPORARY MEMBERS. Temporary members shall be persons who, by resolution of the Management
                            Committee, are allowed the privileges of membership while engaged in any match or competition.
                        </div>
                        <div>
                            All classes of membership shall be subject to the provision of the prevailing Liquor Act.
                            Membership in terms of sub clause 7.1.2 shall not be conferred on or granted to any person who has
                            been suspended, called upon to resign, or expelled from the Club in terms of clauses 11.1
                        </div>
                        <div>
                            7.1.6 COUNTRY MEMBERS. Persons residing outside the boundaries of the jurisdiction of the Sables
                            bowling district may be admitted as country members of the club on compliance with the provisions of
                            sections 8.1 to 8.5 above upon payment of the same entry fee as ordinary members together with such
                            annual membership subscriptions as fixed by the Management Committee. Such country members shall
                            have full privileges as ordinary members excepting the right to vote and serve on the committee/s.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>8. APPLICATION FOR MEMBERSHIP</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            8.1 Every application for membership of this Club, except as a Social Member, shall be made on a
                            form prescribed by Bowls South Africa, which is available at the Club.
                        </div>
                        <div>
                            8.2 Each application form together with the applicant’s details must contain the printed name
                            and signature of both the Proposer and the Seconder of the applicant. The Proposer and Seconder
                            shall have been shall have been members of this club for at least one (1) year and in good
                            standing and shall be addressed to the Honorary Secretary.
                        </div>
                        <div>
                            8.3 The duly completed application form must be exhibited on the Club notice board for at least
                            fourteen (14) days prior to consideration by the Management Committee.
                        </div>
                        <div>
                            8.4 Any member objecting to the acceptance of the proposed member shall notify the Honorary
                            Secretary in writing and on the invitation of the Management Committee, may make verbal
                            representations at the meeting when the application is considered.
                        </div>
                        <div>
                            8.5 The acceptance of Prospective Members shall be by a show of hands at a meeting of the
                            Management Committee. The Nominee shall be accepted if a two-thirds (2/3) majority is obtained.
                        </div>
                        <div>
                            8.6 Subject to acceptance, the applicant will be required to pay the current Entrance Fee and
                            Annual Subscription (or part thereof) as provided for in Section 9, plus any other fees or levies
                            payable, within thirty (30) days of date of acceptance, failing which the Membership status could be
                            rescinded. In appropriate circumstances the Management Committee may grant extension for the payment
                            of the annual subscriptions on such terms and conditions it deems suitable.
                        </div>
                        <div>
                            8.7 The Management Committee has the right to refuse any application for membership without giving
                            any reason for such refusal.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>9. ENTRANCE FEES AND SUBSCRIPTIONS</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            9.1 Entrance fees and Annual Subscriptions, over and above such Affiliation Fees payable to Bowls
                            South Africa and Sables, plus any other subscriptions or levies, shall be determined by the
                            Management Committee. The Management Committee shall be entitled, at its discretion and in
                            appropriate circumstances, to grant rebates or reductions to members on condition that such rebates
                            or reductions do not contravene clause 9.2
                        </div>
                        <div>
                            9.2 Any subscription laid down shall not be less than the minimum amount as laid down by the Liquor
                            Act.
                        </div>
                        <div>
                            9.3 Ordinary Members accepted after the 31st March in each year will be required to pay a pro-rata
                            portion of the Annual Subscription as determined by the Management Committee, plus the full
                            Affiliation Fees payable to Bowls South Africa and Sables and the Entrance Fee due to the club.
                        </div>
                        <div>
                            9.4 Ordinary Members accepted after the last day of June each year shall pay the Entrance Fee only;
                        </div>
                        <div>
                            9.5 For Students under the age of twenty five (25) years, no Entrance Fee is payable
                        </div>
                        <div>
                            9.6 For Students under the age of twenty five (25) years only fifty percent (50%) of the Annual
                            Subscription will be payable, plus the full Affiliation Fees payable to Bowls South Africa and
                            Sables.
                        </div>
                        <div>
                            9.7 All subscriptions are payable in advance and shall be paid before the commencement of the
                            Annual General Meeting. Unpaid members shall not be eligible to cast any vote or to speak on any
                            matter at the Annual General Meeting or any adjournment thereof
                        </div>
                        <div>
                            9.8 Any member whose subscription has not been paid by the fifteenth (15th) day of November of that
                            year, shall cease to be a member of the club
                        </div>
                        <div>
                            9.9 Application by a former member who has forfeited his /her membership in terms of sub clause 9.8
                            hereof shall only be considered by the Management Committee when any arrears subscription and any
                            other monies which may have been due to the club have been paid in full
                        </div>
                        <div>
                            9.10 The names of members who have forfeited their membership in terms of sub clause 9.8 hereof
                            shall be reported immediately to the Secretary of the Sables Bowling Association, as defaulters.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>10. LIABILITY</strong>
                        </div>
                        <div>
                            The liability of members shall be limited to the amount of their unpaid subscriptions and any other
                            monies, which may be owed by them to the Club.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>11. DISCIPLINE</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            11.1 The Management Committee shall be obliged to take appropriate disciplinary action against
                            any of its individual members who to its knowledge contravenes any applicable code of conduct,
                            rule, bylaw or resolution of the Management Committee, Sables District or Bowls SA or who
                            commits any act deemed to be harmful to the sport of bowls or any other member or person at
                            any club, bowls facility or bowls event or any other act which brings the sport into disrepute.
                        </div>
                        <div>
                            11.2 Should it be brought to the attention of the Management Committee that any member may
                            have committed such a contravention it shall forthwith appoint a Disciplinary Convener (a
                            senior member of the club) who will co-opt two or more senior members to form a Disciplinary
                            Committee to deal with the matter.
                        </div>
                        <div>
                            11.3 In the event of a complaint made by Sables, BSA, any other club an official written
                            complaint should be furnished with full details and in the event of a complaint from a member
                            of the Club or the Management Committee such complaint should be given in writing with full
                            details of the alleged contravention (in such event the Honorary Secretary may assist the
                            complainant to formulate the complaint).
                        </div>
                        <div>
                            11.4 Any complaint, to be considered by the Disciplinary Committee, should concern an
                            incident or behavior that occurred no longer than twenty one (21) days before the complaint
                            is lodged.
                        </div>
                        <div>
                            11.5 The Disciplinary Committee shall forthwith investigate the complaint and shall give at
                            least seven (7) days’ notice to the member concerned to appear before the Disciplinary
                            Committee to give an explanation of his alleged conduct. Such enquiry should be held within
                            three (3) weeks of the complaint being received.
                        </div>
                        <div>
                            11.6
                        </div>
                        <div>
                            11.6.1 At such enquiry, the said member may appear in person and may be represented by another
                            member of the club duly authorized by him in writing. He shall not be entitled to legal
                            representation.
                        </div>
                        <div>
                            11.6.2 A statement of the reasons for the proposed enquiry and of the conduct alleged, should
                            be made on behalf of the Management Committee and the member concerned or his representative shall
                            be given an opportunity of denying or explaining the conduct complained of. If evidence is
                            given by or on behalf of the complainant the member or his representative shall be entitled
                            to cross-examine the complainant or the witnesses.
                        </div>
                        <div>
                            11.6.3 If the member gives evidence the complainant shall have the right to cross-examine him.
                        </div>
                        <div>
                            11.6.4 The Chairman of the Disciplinary Committee shall have the right to put questions to
                            any of the parties to clarify any aspect necessary
                        </div>
                        <div>
                            11.6.5 Should such member fail to appear personally, as provided above, the Disciplinary
                            Committee may deal with the matter in the absence of such member or his representative.
                        </div>
                        <div>
                            11.6.6 The decision of the Disciplinary Committee shall be conveyed to the member, in writing,
                            within three (3) days of the date of the meeting at which the matter was considered and be
                            referred to the Management Committee with a recommendation as to the sanction to be applied.
                        </div>
                        <div>
                            11.6.7 A proper record shall be kept by the Honorary Secretary of the proceedings at the
                            enquiry which will be admissible at any later appeal.
                        </div>
                        <div>
                            11.8 The Management Committee shall have power to:
                        </div>
                        <div>
                            11.8.1 Deprive such member of any or all of the rights, privileges and advantages of
                            membership during such time as the Management Committee may deem fit, or
                        </div>
                        <div>
                            11.8.2 Issue a verbal or written warning to such member, or
                        </div>
                        <div>
                            11.8.3 Suspend such member from membership of the Club for such period as it deems fit
                            (such period to be a maximum of one (1) year), or
                        </div>
                        <div>
                            11.8.4 Call on such member, in writing, through the Honorary Secretary to resign and, if he
                            fails to do so within seven (7) days of the date of such notice, expel such member, or
                        </div>
                        <div>
                            11.8.5 Expel such member.
                        </div>
                        <div>
                            11.9 Any member, who has been dealt with in terms of this section, shall have a right of
                            appeal against the decision of the Disciplinary Committee and/or the Management Committee to
                            Sables District. Such appeal shall be submitted, in writing, to Sables within seven (7) days
                            of receipt by the member of the decision of the Management Committee. Any suspension,
                            resignation or expulsion shall remain in force pending such an appeal.
                        </div>
                        <div>
                            11.10 Any member dealt with in terms of this section and if the Disciplinary Committee’s or
                            Management Committee’s decision has not been overruled on appeal, shall be ineligible for
                            re-election as a member for such period as the Management Committee at its absolute
                            discretion may decide (being a maximum period of one (1) year) and shall have no claim
                            whatsoever upon the property of the Club nor for a refund of any subscription or portion
                            thereof.
                        </div>
                        <div>
                            11.11 During the period that any member is under suspension or has been expelled and pending
                            an appeal, such member is debarred from entering the confines of the Club.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>12. RESIGNATION</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            12.1 A Member may at any time, by giving notice in writing to the Honorary Secretary, resign
                            his/her membership of the Club. However to avoid liability for Annual Subscriptions for the ensuing
                            season, a written resignation must be received by the Honorary Secretary no later than 31st July,
                            being the last day of the Club’s financial year.
                        </div>
                        <div>
                            12.2 A Member who has resigned membership shall continue to be liable for any subscription or other
                            monies owing to the Club due and unpaid at the time of resignation.
                        </div>
                        <div>
                            12.3 If the Member is in good financial standing the Management Committee shall authorize the issue
                            of a Clearance Certificate, if requested.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>13. VISITORS and GUESTS</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            13.1 Any person, not a Member of the Club, visiting the club as a guest or visitor shall be
                            introduced by a member. The name of such visitor or guest shall be entered in the Visitor’s Book by
                            the person introducing him/her and shall, at the same time; sign his/her own name in the book
                            opposite the name of his/her guest or visitor.
                        </div>
                        <div>
                            13.2 The member introducing the visitor or guest shall be responsible for the behavior of such
                            person and shall remain on the Club premises during that guest or visitor’s presence.
                        </div>
                        <div>
                            13.3 No person who has been refused membership in terms of clause 11.10 or a defaulter in terms of
                            clause 9.10 shall be introduced as a visitor or guest.
                        </div>
                        <div>
                            13.4 The Management Committee shall have the right to refuse admission to any visitor or guest.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>14. INSURANCE</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            The members at the Annual General Meeting shall decide on the insurer and contents of the
                            policy of of insurance regarding the assets of the club, at each such meeting. Personal
                            effects of Members shall not be covered by this clause.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>15. FINANCIAL</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            15.1 The financial year of the Club shall commence on the first day of August each year.
                        </div>
                        <div>
                            15.2 No liabilities shall be incurred without sanction of the Management Committee. Any payment,
                            which, in the opinion of the Executive Committee, requires urgent attention, shall be dealt with
                            urgently. However such payment must be authorized at the following meeting of the Management
                            Committee.
                        </div>
                        <div>
                            15.3 The Banking account of the Club shall be kept in the name of the Krugersdorp Bowling Club at
                            any bank, which the Management Committee may decide upon. Cheques drawn on this account shall be
                            signed by any two of the following:
                        </div>
                        <div>
                            15.3.1 The President
                        </div>
                        <div>
                            15.3.2 The Vice-President
                        </div>
                        <div>
                            15.3.3 The Honorary Secretary
                        </div>
                        <div>
                            15.3.4 The Honorary Treasurer
                        </div>
                        <div>
                            15.3.5 Such other member/s of the Management Committee as authorized by resolution of the
                            Management Committee.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>16. ANNUAL GENERAL MEETING</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            16.1 The Annual General Meeting of the Club shall be held during September in each year at such
                            time and at such place as the Management Committee may decide.
                        </div>
                        <div>
                            16.2 Fifteen (15) days' notice of the meeting shall be given to all Life/Ordinary Members.
                        </div>
                        <div>
                            16.3 The non-receipt of such notice by any member shall not invalidate the proceedings at any
                            General Meeting.
                        </div>
                        <div>
                            16.4 A copy of the agenda and a copy of all proposed amendments, additions or deletions to the
                            Constitution shall accompany the notice of meeting.
                        </div>
                        <div>
                            16.5 All other documents including financial statements shall be available at the clubhouse
                            fifteen (15) days before the date of the meeting.
                        </div>
                        <div>
                            16.6 The business to be transacted shall be:
                        </div>
                        <div>
                            16.6.1 To read the notice convening the meeting.
                        </div>
                        <div>
                            16.6.2 To read and confirm the minutes of the previous Annual General Meeting and any Special
                            General Meetings held since the previous Annual General Meeting and to deal with any business
                            arising there from.
                        </div>
                        <div>
                            16.6.3 To receive the President’s Report and certified Statements of Account for the previous year.
                        </div>
                        <div>
                            16.6.4 To deal with any correspondence.
                        </div>
                        <div>
                            16.6.5 To elect a Management Committee for the ensuing year.
                        </div>
                        <div>
                            16.6.6 To elect Selection Committee for the ensuing year.
                        </div>
                        <div>
                            16.6.7 To elect an examiner/examiners for the ensuing year.
                        </div>
                        <div>
                            16.6.8 To transact any other business that the meeting is entitled to conduct.
                        </div>
                        <div>
                            16.7 Only fully paid Members and Life Members are entitled to vote or speak on any matter at the
                            meeting.
                        </div>
                        <div>
                            16.8 25% of the members entitled to vote shall constitute a quorum at any Annual General Meeting.
                            Should a quorum not be present within thirty (3) minutes of the time specified for the opening of
                            the meeting, it shall stand adjourned for seven (7) days to be held at the same time and place and
                            the members then present shall be competent to transact the business for which the meeting was
                            originally called.
                        </div>
                        <div>
                            16.9 The President or in his/her absence, the Vice President shall preside at all Annual General
                            Meetings but, in the absence of both, the meeting shall elect its own Chairperson.
                        </div>
                        <div>
                            16.10 The election of office bearers and members of the Management Committee and Selection
                            Committee shall be decided by secret ballot. The Chairperson shall appoint, when a ballot is taken,
                            two (2) scrutineers.
                        </div>
                        <div>
                            16.11 All other matters submitted to the meeting shall be decided upon by a show of hands.
                        </div>
                        <div>
                            16.12 Any three (3) members present and entitled to vote may demand a ballot which request must be
                            acceded to.
                        </div>
                        <div>
                            16.13 Notices of Motion for consideration at the Annual General Meeting must be in writing and in
                            the hands of the Honorary Secretary, not later than the first (1st) September prior to such Annual
                            General Meeting.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>17. SPECIAL GENERAL MEETINGS</strong>
                        </div>
                        <div>
                            17.1 Special General Meetings may be convened by the Management Committee and shall be convened on
                            a requisition in writing of not less than 25% of members in good standing, stating the purpose for
                            which the meeting is required.
                        </div>
                        <div>
                            17.2 No business shall be transacted at any Special General Meeting other than that for which it
                            was called.
                        </div>
                        <div>
                            17.3 Fourteen (14) days notice shall be given to members and such notice shall state the purpose
                            for which the meeting was called.
                        </div>
                        <div>
                            17.4 The provisions of clauses 16 shall mutatis mutandis apply to the conduct of and procedures at
                            Special General Meetings.
                        </div>
