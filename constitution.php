                            <strong>21. INFRINGEMENT OF THE PROVISIONS OF THE CONSTITUTION AND BY LAWS</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            The Management Committee shall take cognizance of any infringement of the Constitution or By Laws
                            and shall deal with the matter in accordance with the procedures set out in this Constitution
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>22. COPIES OF THE CONSTITUTION AND BY LAWS</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            22.1 A copy of the Constitution and By Laws shall, at all times, be exhibited on the Club notice
                            board.
                        </div>
                        <div>
                            22.2 A copy of the Constitution and By Laws shall be supplied free of charge to each new member at
                            the time he/she is accepted as a member. Additional copies of these documents shall be available to
                            existing members at a cost laid down by the Management Committee.
                        </div>
                        <div>
                            22.3 Members shall be deemed to be conversant with the contents of the Constitution and By
                            Laws including the disciplinary provisions.
                        </div>
