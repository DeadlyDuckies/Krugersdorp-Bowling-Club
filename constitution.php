                            <strong>BY-LAWS</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            1. All games played in connection with the Club shall be played in accordance with the rules of the
                            game laid down by Bowls South Africa.
                        </div>
                        <div>
                            2. The greens shall only be available to those person provided for in the Constitution.
                        </div>
                        <div>
                            3. Visitors to the Club and members who may wish to play bowls may be required to pay green fees
                            the amount of which will be determined by the Management Committee.
                        </div>
                        <div>
                            4. The Management Committee shall decide who is entitled to play on the greens. Any prospective
                            bowler shall be coached by a registered coach prior to him participating in the game of bowls.
                        </div>
                        <div>
                            5. No game other than bowls shall be played on the greens and players shall not be permitted to use
                            the greens except when wearing recognized bowling shoes.
                        </div>
                        <div>
                            6. The Management Committee or any other authorized official shall decide whether the greens are in
                            a suitable condition for play.
                        </div>
                        <div>
                            7. All players wishing to participate in social bowls shall given their tabs and green fees to the
                            duty officer before the scheduled time.
                        </div>
                        <div>
                            8. The scheduled times for play shall be decided by the Management Committee.
                        </div>
                        <div>
                            9. Mixed bowls i.e. ladies and men shall be played on such days as determined by the Management
                            Committee.
                        </div>
                        <div>
                            10. When playing social bowls on recognized playing days or participating in Club competitions
                            members shall conform to the dress regulations as laid down by Bowls South Africa .
                        </div>
                        <div>
                            11. When worn it shall be incumbent on members to wear the Club colours when engaged in social
                            bowls or Club competitions. Likewise when entering Provincial competitions or Bowls South Africa
                            National Championships through the Club, then Krugersdorp Bowling Club colours shall be worn.
                        </div>
                        <div>
                            12. The Management Committee shall decide upon the entrance fees the Club competitions
                        </div>
                        <div>
                            13. The Management Committee shall decide upon the conditions of play for all Club competitions
                        </div>
                        <div>
                            14. The Management Committee shall have the right to reserve the greens at any time for matches or
                            Bounce games and shall make all arrangements in connection therewith. The Management or any member
                            authorized by it shall have the right to call on any member either in writing or verbally to act as
                            a marker in any singles game provided that the member so requested is a participant in the event in
                            which he/she has been asked to mark
                        </div>
                        <div>
                            15. Club competitions shall be open to all Life and Ordinary members. The Management Committee may
                            exclude from the singles Championship those members who elect a club other than the Krugersdorp
                            Bowling Club as their club of preference. A “Club of Preference” is as determined by the Sables
                            Bowling Association.
                        </div>
                        <div>
                            16. The Novices competition shall be confined to members who have less than three (3) years bowling
                            experience and who have not won a singles competition.
                        </div>
                        <div>
                            17. Club competitions shall have preference in the allocation of rinks provided that at least four
                            (4) rinks are made available to those members wishing to play social bowls.
                        </div>
                        <div>
                            18. The Management Committee may, on request, authorize the running of special competitions if
                            satisfied that such events will not interfere with those already established.
                        </div>
                        <div>
                            19. The bar hours shall be as determined by the Management Committee.
                        </div>
                        <div>
                            20. The club premises shall be closed on such days and at such times as the Management Committee
                            may, from time to time, think fit.
                        </div>
                        <div>
                            21. Dog owners are permitted to bring their dogs onto the property only when the dog is under
                            constant supervision and strict control of the owner/handler. The owner is liable for any
                            damage incurred or disruptive behaviour by the dog whilst on the clubs property. Dogs must be
                            kept on a leash at all times.
                        </div>
                        <div>
                            22. Children entering the premises and clubhouse should be on their best behaviour. They
                            should not be allowed onto the greens without management committee approval and under adult
                            supervision, who is responsible for their conduct. Parents and guardians are liable for any
                            damages caused by minors, should they be present or not. Under no circumstances will
                            children under the age of 18 be allowed to order alcohol.
                        </div>
                        <div>
                            23. The following are strictly forbidden:
                        </div>
                        <div>
                            23.1 Playing or practicing without mats.
                        </div>
                        <div>
                            23.2 Dropping of litter of any sort on the greens, banks or in the ditches.
                        </div>
                        <div>
                            23.3 Intentionally dropping of bowls on the green from the standing position.
                        </div>
                        <div>
                            23.4 No vehicles shall be allowed beyond the barrier unless the Management Committee has granted
                            permission.
                        </div>
                        <div>
                            24. No prizes will be awarded to any member unless such member’s current years subscriptions and
                            all other dues have been paid
                        </div>
                        <div>
                            25. The sale of goods or tickets relating to any sweep or competition on the Club premises shall be
                            subject to the permission of the Management Committee.
