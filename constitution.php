                        <div>
                            <strong>18. PROCEDURES TO BE FOLLOWED IN THE ELECTION OF THE MANAGEMENT COMMITTEE</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            18.1 On or before the Nineteenth (19th) July, in each year the Honorary Secretary shall place a
                            notice on the Club notice board calling for nominations for the various office bearers and committee
                            members This notice shall be exhibited until the first (1) September.
                        </div>
                        <div>
                            18.2 Nominations shall be made on forms, which shall be obtainable from the Honorary Secretary. The
                            proposer and seconder who shall be members in good standing and who have been members for at least
                            two (2) years shall sign the nomination form containing the name of the nominee. The nominee
                            indicating that he/she agrees to accept nomination to the post designated shall also sign the
                            nomination form. The completed nomination form shall be handed to the Honorary Secretary or placed
                            in the box provided, not later than the first (1st) September, after which date no further
                            nominations shall be accepted, save as set out in sub-clause 18.6 thereof.
                        </div>
                        <div>
                            18.3 No member of less than one (1) years standing is eligible for nomination for any office or as
                            a Committee Member.
                        </div>
                        <div>
                            18.4 The President, Vice President, the Honorary Secretary, and the Honorary Treasurer shall be
                            elected separately in that order. Thereafter the remaining members of the Management Committee shall
                            be elected. Members may vote for any number up to the maximum number of members required for the
                            Management Committee but shall cast no more than one (1) vote for each candidate. Any ballot paper
                            not complying with this requirement shall be declared null and void.
                        </div>
                        <div>
                            18.5 Any eligible member may be nominated for more than one (1) office but should he/she be elected
                            to one office his/her nomination for any other office shall automatically fall away.
                        </div>
                        <div>
                            18.6 If, at the Annual General Meeting, there is no nomination for any of the office bearers or
                            committee members referred to in sub-clause 18.4, or the person nominated has died, or has withdrawn
                            his/her acceptance of nomination, or for any reason is unable to or permitted to accept office, then
                            the Chairperson of the meeting shall call for further nominations for such office from members
                            present at the meeting. The procedure referred in sub-clause 18.4 shall thereafter be followed,
                            provided that if any nomination for election to the Management Committee has been withdrawn or for
                            any reason cannot take effect no further nominations shall be called for and a ballot shall be taken
                            of the remaining candidates so nominated shall be declared elected and further nominations shall
                            then be called for an balloted upon to make up the deficiency.
                        </div>
