                            <strong>19. AMENDMENTS TO THE CONSTITUTION</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            19.1 No alteration of, or addition to, the Constitution shall be made except at an Annual General
                            Meeting or at a Special General Meeting. At least thirty (30) days notice shall be given, in writing
                            by the Honorary Secretary in respect of such Special General Meeting setting out in full each
                            proposed deletion, addition or alteration.
                        </div>
                        <div>
                            19.2 No alteration whatsoever to the Constitution shall be effected unless carried by a minimum 75%
                            of the votes represented at the meeting.
                        </div>
                        <div>
                            19.3 The text of any proposed alteration or addition shall be exhibited on the notice board for at
                            least fourteen (14) days prior to the date of the meeting.
                        </div>
