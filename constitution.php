                            <strong>20. INTERPRETATION OF THE CONSTITUTION AND BY LAWS</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            In the case of doubt as to the meaning or interpretation of the Constitution or By Laws, the
                            Chairperson at the Annual General Meeting or a Special General Meeting or the Management Committee
                            at all other times, shall be the final arbiter. The ruling of the Chairperson or the Management
                            Committee, as the case may be shall be binding on all members.
                        </div>
                        <div>
                            <br>
                        </div>
