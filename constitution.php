                            <strong>23. COMPLAINTS AND SUGGESTIONS</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            All complaints and suggestions should be made in writing to the Honorary Secretary; who, in
                            turn, shall bring such to the notice of the Management Committee. The Management Committee
                            shall address all such correspondence and give feedback on the subject topic to the Honorary
                            Secretary who shall in turn convey such communication to the relevant person/s who lodged the
                            complaint or suggestion
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>24. DISSOLUTION OF THE CLUB</strong>
                            <span style="font-size: 1rem;"></span>
                        </div>
                        <div>
                            Should the Club at any stage be wound up, liquidated or dissolved, any asset remaining after all
                            the club’s liabilities have been met, shall be transferred to another non-profit organization having
                            similar objects.
                        </div>
                        <div>
                            <br>
                        </div>
                        <div>
                            <strong>25. EFFECTIVE DATE OF CONSTITUTION</strong>
                        </div>
